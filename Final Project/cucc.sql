CREATE DATABASE cucc;
USE cucc;

CREATE TABLE workers (
	wid INTEGER PRIMARY KEY NOT NULL,
	wname varchar(30) 
);

CREATE TABLE supervisors (
	sid INTEGER PRIMARY KEY NOT NULL,
	sname varchar(30) NOT NULL,
	sdateapproval varchar(30) DEFAULT NULL, 
	sdatedecline varchar(30) DEFAULT NULL,
	sdatesubmission varchar(30) 
);

CREATE TABLE worked (
	wkd_no INTEGER PRIMARY KEY NOT NULL,
	wkd_id INTEGER NOT NULL,
	wkd_date varchar(30) NOT NULL,
	wkd_rate INTEGER DEFAULT '13',
	wkd_hours DECIMAL DEFAULT NULL
);

CREATE TABLE workeds (
	wkds_no INTEGER PRIMARY KEY NOT NULL,
	wkds_id INTEGER NOT NULL,
	wkds_date varchar(30) NOT NULL,
	wkds_rate INTEGER DEFAULT '15',
	wkds_hours DECIMAL DEFAULT NULL
);

CREATE TABLE payroll (
	pno INTEGER PRIMARY KEY NOT NULL,
	pdates varchar(30) NOT NULL,
	pid INTEGER NOT NULL,
	pgross REAL DEFAULT NULL
);

